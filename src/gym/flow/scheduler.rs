/// Represents the specific type of business event occurring in the flow lifecycle.
#[derive(Debug, Clone)]
pub enum RfqEvent {
    /// **Inbound Request:** The Generator has produced a new client interest.
    /// The agent must now decide whether to quote.
    NewRequest(Rfq<Open>),

    /// **Client Feedback:** The probabilistic model has made a decision on the agent's quote.
    CustomerReply {
        rfq_id: RfqId,
        decision: CustomerDecision,
    },

    /// **Timeout:** The RfQ's `time_to_live` has elapsed without a finalized deal.
    Expired {
        rfq_id: RfqId,
    },

    WakeUp,
}

/// The outcome of the client's decision-making process.
#[derive(Debug, Clone, Copy, PartialEq, Serialize, Deserialize)]
pub enum CustomerDecision {
    /// Client accepted the price. Trade is booked.
    Accept,

    /// Client rejected the price (too far from fair value).
    Reject,

    /// Client proposes a new price (Negotiation).
    Counter(Price),
}

impl CustomerDecision {
    pub fn is_accept(&self) -> bool {
        matches!(self, CustomerDecision::Accept)
    }

    pub fn is_reject(&self) -> bool {
        matches!(self, CustomerDecision::Reject)
    }

    pub fn is_counter(&self) -> bool {
        matches!(self, CustomerDecision::Counter(_))
    }

    pub fn counter_price(&self) -> Option<Price> {
        match self {
            CustomerDecision::Counter(price) => Some(*price),
            _ => None,
        }
    }

    pub fn requires_agent_reply(&self) -> bool {
        self.is_accept() || self.is_counter()
    }
}

/// A wrapper for events that need to be scheduled in the future.
///
/// Implements `Ord` based on `timestamp` to function within a Priority Queue.
#[derive(Clone, Debug)]
pub struct ScheduledEvent {
    pub timestamp: DateTime<Utc>,
    pub event_type: RfqEvent,
}

impl PartialEq for ScheduledEvent {
    fn eq(&self, other: &Self) -> bool {
        self.timestamp == other.timestamp
    }
}

impl Eq for ScheduledEvent {}

impl PartialOrd for ScheduledEvent {
    fn partial_cmp(&self, other: &Self) -> Option<Ordering> {
        Some(self.cmp(other))
    }
}

impl Ord for ScheduledEvent {
    fn cmp(&self, other: &Self) -> Ordering {
        self.timestamp.cmp(&other.timestamp)
    }
}

